package ReplHomeworks;

public class ReplHW69 {

	public static void main(String[] args) {
		
		for(int N2=7; N2>1; N2--) {
			for(int R3=1; R3<=N2; R3++) {
				System.out.print(R3+" ");
			}
			System.out.println();
		}
		
		for(int R=1; R<=7; R++) {
			
			for(int C=1; C<=R; C++) {
				System.out.print(C+" ");
			}
			System.out.println();
	}

	}
}
